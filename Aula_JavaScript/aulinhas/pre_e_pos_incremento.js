let n = 10; 

console.log(n++); // primeiro se usa o valor antes de incrementa-lo ou descementa-lo
console.log(++n); // agora incrementa ou decrementa o valor e então o imprime

console.log(n +""+ 5); // o mais(+) junto a string será intepretado como sinal de concatenação

